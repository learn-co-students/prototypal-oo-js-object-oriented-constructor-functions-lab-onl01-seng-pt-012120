class Scooter {
    constructor(x, y, z) {
        this.year = x
        this.color = y
        this.model = z
    }
}

class Driver {
    constructor(name, age, experience) {
        this.name = name
        this.age = age
        this.experience = experience
    }
}

class PickupLocation {
    constructor(address, city) {
        this.address = address
        this.city = city
    }
}